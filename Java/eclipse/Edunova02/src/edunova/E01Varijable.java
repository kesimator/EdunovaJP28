package edunova;

public class E01Varijable {

	public static void main(String[] args) {
		
		// Varijabla je prostor u memoriji
		// Java je type strict PL (programming language)
		
		// int je tip podatka
		// i je naziv
		
		
		
		// 1. korak: deklaracija varijable
		int i;
		
		
		// 2. korak: dodjeljivanje vrijednosti
		i=2;
		
		
		
		// 3. korak: korištenje
		System.out.println(i);
		
		
		
		// Brže sintakse
		
		int j=2;
		
		int k,l,o=2;
		
		
		
		i=9;
		
		
	}

}
